// Current language (defaults to Arabic)
let currentLang = localStorage.getItem('benshop_lang') || 'ar';

// Translate helper
function t(key) {
  return (translations[currentLang] && translations[currentLang][key]) || translations.ar[key] || key;
}

// Apply translations to all elements with data-i18n attribute
function applyTranslations(lang) {
  currentLang = lang;
  localStorage.setItem('benshop_lang', lang);

  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';

  // Apply all data-i18n elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key);
    if (val && val !== key) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = val;
      } else {
        el.textContent = val;
      }
    }
  });

  // Apply data-i18n-placeholder for inputs
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    const val = t(key);
    if (val && val !== key) el.placeholder = val;
  });

  // Apply data-i18n-aria for aria-labels
  document.querySelectorAll('[data-i18n-aria]').forEach(el => {
    const key = el.getAttribute('data-i18n-aria');
    const val = t(key);
    if (val && val !== key) el.setAttribute('aria-label', val);
  });

  // Update language toggle button text
  const langBtn = document.getElementById('langToggle');
  if (langBtn) langBtn.textContent = lang.toUpperCase();

  // Load or unload RTL stylesheet
  let rtlLink = document.getElementById('rtl-stylesheet');
  if (lang === 'ar') {
    if (!rtlLink) {
      rtlLink = document.createElement('link');
      rtlLink.id = 'rtl-stylesheet';
      rtlLink.rel = 'stylesheet';
      rtlLink.href = 'rtl.css';
      document.head.appendChild(rtlLink);
    }
  } else if (rtlLink) {
    rtlLink.remove();
  }

  // Update page title and meta tags based on current page
  const page = location.pathname.split('/').pop().replace('.html', '') || 'index';
  const titleMap = {
    '': 'page_title_home', 'index': 'page_title_home',
    'catalog': 'page_title_catalog', 'product': 'page_title_product',
    'contact': 'page_title_contact', 'cookies': 'page_title_cookies',
    'privacy': 'page_title_privacy', 'terms': 'page_title_terms',
  };
  const titleKey = titleMap[page];
  if (titleKey && t(titleKey) !== titleKey) {
    document.title = t(titleKey);
    const metaDesc = document.querySelector('meta[name="description"]');
    if (metaDesc) { const dk = 'meta_desc_' + (page || 'index'); if (t(dk) !== dk) metaDesc.content = t(dk); }
    const ogTitle = document.querySelector('meta[property="og:title"]');
    if (ogTitle) ogTitle.content = t(titleKey);
    const ogDesc = document.querySelector('meta[property="og:description"]');
    if (ogDesc) { const dk = 'meta_desc_' + (page || 'index'); if (t(dk) !== dk) ogDesc.content = t(dk); }
    const twTitle = document.querySelector('meta[name="twitter:title"]');
    if (twTitle) twTitle.content = t(titleKey);
    const twDesc = document.querySelector('meta[name="twitter:description"]');
    if (twDesc) { const dk = 'meta_desc_' + (page || 'index'); if (t(dk) !== dk) twDesc.content = t(dk); }
  }

  // Dispatch event for script.js to react
  window.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang } }));
}