// Admin language state (separate from frontend)
let adminLang = localStorage.getItem('benshop_admin_lang') || 'ar';

// Admin translate helper
function ta(key) {
  return (adminTranslations[adminLang] && adminTranslations[adminLang][key]) || adminTranslations.ar[key] || key;
}

// Apply admin translations
function applyAdminTranslations(lang) {
  adminLang = lang;
  localStorage.setItem('benshop_admin_lang', lang);
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';

  // Apply all data-i18n elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = ta(key);
    if (val && val !== key) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = val;
      } else {
        el.textContent = val;
      }
    }
  });

  // Apply data-i18n-ph for inputs
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    const val = ta(key);
    if (val && val !== key) el.placeholder = val;
  });

  // Update language toggle button
  const langBtn = document.getElementById('adminLangToggle');
  if (langBtn) langBtn.textContent = lang.toUpperCase();

  // Load or unload RTL stylesheet for admin
  let rtlLink = document.getElementById('admin-rtl-stylesheet');
  if (lang === 'ar') {
    if (!rtlLink) {
      rtlLink = document.createElement('link');
      rtlLink.id = 'admin-rtl-stylesheet';
      rtlLink.rel = 'stylesheet';
      rtlLink.href = 'admin-rtl.css';
      document.head.appendChild(rtlLink);
    }
  } else if (rtlLink) {
    rtlLink.remove();
  }

  // Update page title
  document.title = ta('admin_title');

  // Dispatch event for admin.js to react
  window.dispatchEvent(new CustomEvent('adminLanguageChanged', { detail: { lang } }));
}